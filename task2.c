#include <stdio.h>

int main() {
    printf("Result = %i\n", 2 + 3 * 5);
    printf("Result = %i\n", 3 + 5 - 2 * 2);
    printf("Result = %i\n", 4 / 6 * 3);
    printf("Result = %i\n", 4 * 3 / 6);
    printf("Result = %f\n", 4 / 6.0 * 3);
    printf("Result = %i\n", 8  - 7 % 5);
    printf("Result = %i\n", 3 + 2 * 4 / 5 - 1 * 6);
}